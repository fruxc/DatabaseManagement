SET SERVEROUTPUT ON;
CREATE OR REPLACE TRIGGER EMP_TRIG
BEFORE INSERT ON EMP_02
FOR EACH ROW
DECLARE
LESS_SALARY EXCEPTION;
BEGIN
IF(:new.SALARY<10000)THEN
   RAISE LESS_SALARY;
END IF;
EXCEPTION
   WHEN LESS_SALARY THEN
      dbms_output.put_line('SALARY SHOULD BE GREATER THAN 10000'); 
END;
/