SET SERVEROUTPUT ON;
DECLARE 
   N1 NUMBER;
   N2 NUMBER;
   N3 NUMBER;
   RES1 NUMBER;
FUNCTION ADD_02(X NUMBER, Y NUMBER) 
RETURN NUMBER 
IS
R NUMBER; 
BEGIN 
   R := X + Y;
RETURN R; 
END ADD_02;  

FUNCTION ADD_02(X NUMBER, Y NUMBER,Z NUMBER) 
RETURN NUMBER  
IS 
R NUMBER; 
BEGIN 
   R := X + Y + Z;
RETURN R; 
END ADD_02;

BEGIN
   N1 := &N;
   N2 := &N;
   N3 := &N;
   RES1 := ADD_02(N1,N2); 
   dbms_output.put_line(' ADDITION OF FIRST TWO NUMBERS ' || RES1);
   RES1 := ADD_02(N1,N2,N3); 
   dbms_output.put_line(' ADDITION OF THREE NUMBERS ' || RES1); 
END; 
/