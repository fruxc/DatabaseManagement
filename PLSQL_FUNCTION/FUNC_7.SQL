SET SERVEROUTPUT ON;
DECLARE 
FUNCTION FIFTH_02
RETURN VARCHAR AS
	NAME VARCHAR(20);
	CURSOR C1 IS SELECT BNAME BM FROM DEPOSIT_02 ORDER BY AMOUNT DESC;
	R C1%ROWTYPE;
BEGIN
FOR R IN C1 LOOP
    IF(C1%rowcount = 5)THEN
        NAME := R.BM;
    END IF;
END LOOP;
RETURN NAME;
END FIFTH_02;

BEGIN
   dbms_output.put_line('NAME OF THE BRANCH WITH FIFTH HIGHEST AMOUNT : ' || FIFTH_02());
END;
