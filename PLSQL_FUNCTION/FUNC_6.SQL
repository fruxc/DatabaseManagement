SET SERVEROUTPUT ON;
DECLARE
CNT NUMBER;
FUNCTION COUNT2_02
return VARCHAR as
	NAME VARCHAR(20);
	CURSOR c1 IS SELECT * FROM DEPOSIT_02 WHERE CNAME LIKE 'A%';
	r c1%ROWTYPE;
BEGIN
CNT:=0;
OPEN c1;
FETCH c1 into r;
WHILE(c1%found)LOOP
    CNT:=CNT+1;
    FETCH c1 INTO r;
END LOOP;
RETURN CNT;
END COUNT2  _02;

BEGIN
   dbms_output.put_line(' NUMBER OF DEPOSITOR WHOSE NAME STARTS WITH A : ' || COUNT2_02());
END;
