SET SERVEROUTPUT ON;
DECLARE
FUNCTION HIGH_02
return VARCHAR as
	NAME VARCHAR(20);
	CURSOR c1 IS SELECT * FROM DEPOSIT_02;
	r c1%ROWTYPE;
	MAX DEPOSIT_02.AMOUNT%TYPE:=0;

BEGIN
	MAX:=0;
	FOR r IN c1 LOOP
		MAX:=r.amount;
		NAME:=r.cname;
	END LOOP;
	RETURN NAME;
END HIGH_02;

BEGIN
   dbms_output.put_line(' NAME OF THE HIGHEST DEPOSITOR IS ' || HIGH_02());
END;
/